#include <unistd.h>

void	ft_print_alphabet(void)
{
	int	abc;

		abc = 'z';
		while (abc >= 'a')
	{
		write(1, &abc, 1);
		abc--;
	}
}

int	main()
{
	ft_print_alphabet();
	return 0;
}
