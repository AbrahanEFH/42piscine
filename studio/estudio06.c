#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}

int 	main()
{
	char *c;

	c = "hello";
	printf("%d", ft_strlen(c));
}
